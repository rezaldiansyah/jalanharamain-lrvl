    <script>
        // Add modal show logic
        document.addEventListener('DOMContentLoaded', function() {
            const modal = document.getElementById('formModal');
            modal.classList.add('show');
            
            // Close modal when clicking outside
            modal.addEventListener('click', function(e) {
                if (e.target === modal) {
                    modal.classList.remove('show');
                }
            });
        });

        // City Select Handler
        document.getElementById('citySelect').addEventListener('change', function() {
            const otherCity = document.getElementById('otherCity');
            otherCity.classList.toggle('hidden', this.value !== 'other');
            otherCity.required = this.value === 'other';
        });

        // Form Submission Handler
        function handleSubmit(event) {
            event.preventDefault();
            const form = event.target.closest('form');
            
            if (!form.checkValidity()) {
                form.reportValidity();
                return;
            }

            const formData = new FormData(form);
            fetch(form.action, {
                method: 'POST',
                body: formData,
                headers: {
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                }
            })
            .then(async (response) => {
                let data;
                try {
                    data = await response.json();
                } catch (e) {
                    data = {};
                }

                if (response.ok && data.success) {
                    // Tutup modal
                    document.getElementById('formModal').classList.remove('show');

                    const redirectUrl = data.redirect_url || 'https://bit.ly/GabungTourLeader';
                    window.location.href = redirectUrl;
                } else {
                    const msgs = data?.errors
                        ? Object.values(data.errors).flat().join('\n')
                        : (data?.message || 'Terjadi kesalahan. Silakan coba lagi.');
                    alert(msgs);
                }
            })
            .catch(error => {
                console.error('Error:', error);
                alert('Terjadi kesalahan. Silakan coba lagi.');
            });
        }
    </script>
